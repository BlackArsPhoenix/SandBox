def font(size: int, bold: bool = False) -> ImageFont.FreeTypeFont:
    return ImageFont.truetype(FONT_BOLD if bold else FONT_REG, size)


def wrap_text(draw: ImageDraw.ImageDraw, text: str, fnt: ImageFont.FreeTypeFont, max_width: int) -> List[str]:
    words = text.replace("\n", " \n ").split(" ")
    lines: List[str] = []
    cur = ""
    for w in words:
        if w == "\n":
            lines.append(cur)
            cur = ""
            continue
        trial = w if not cur else f"{cur} {w}"
        if draw.textlength(trial, font=fnt) <= max_width:
            cur = trial
        else:
            if cur:
                lines.append(cur)
            cur = w
    if cur:
        lines.append(cur)
    return lines


def draw_panel(
    base: Image.Image,
    box: Tuple[float, float, float, float],
    title: Optional[str],
    body: str,
    title_size: int = 15,
    body_size: int = 11,
    padding: int = 10,
    solid: bool = False,
) -> None:
    """box = (x0,y0,x1,y1) as fractions of W,H."""
    x0, y0, x1, y1 = int(box[0] * W), int(box[1] * H), int(box[2] * W), int(box[3] * H)
    overlay = Image.new("RGBA", base.size, (0, 0, 0, 0))
    d = ImageDraw.Draw(overlay)
    fill = PARCHMENT_SOLID if solid else PARCHMENT
    d.rounded_rectangle([x0, y0, x1, y1], radius=10, fill=fill, outline=PANEL_BORDER, width=2)
    base.alpha_composite(overlay)
    draw = ImageDraw.Draw(base)

    f_title = font(title_size, bold=True)
    f_body = font(body_size)
    cx = x0 + padding
    cy = y0 + padding
    max_w = x1 - x0 - 2 * padding

    if title:
        draw.text((cx, cy), title, font=f_title, fill=TITLE_COLOR)
        cy += title_size + 8

    line_h = body_size + 3
    for para in body.split("\n\n"):
        for line in wrap_text(draw, para.strip(), f_body, max_w):
            if cy + line_h > y1 - padding:
                return
            draw.text((cx, cy), line, font=f_body, fill=BODY_COLOR)
            cy += line_h
        cy += 4


def draw_banner(base: Image.Image, text: str) -> None:
    overlay = Image.new("RGBA", base.size, (0, 0, 0, 0))
    d = ImageDraw.Draw(overlay)
    y0 = int(0.88 * H)
    d.rectangle([0, y0, W, H], fill=(40, 28, 18, 210))
    base.alpha_composite(overlay)
    draw = ImageDraw.Draw(base)
    fnt = font(13, bold=True)
    lines = wrap_text(draw, text, fnt, W - 40)
    cy = y0 + 12
    for line in lines:
        tw = draw.textlength(line, font=fnt)
        draw.text(((W - tw) / 2, cy), line, font=fnt, fill=(255, 245, 220))
        cy += 18


def draw_footer_ru(base: Image.Image, page_no: int) -> None:
    draw = ImageDraw.Draw(base)
    fnt = font(9, bold=True)
    label = f"RU · {page_no}"
    # small badge top-right
    tw = draw.textlength(label, font=fnt)
    x0, y0 = W - tw - 18, 8
    overlay = Image.new("RGBA", base.size, (0, 0, 0, 0))
    d = ImageDraw.Draw(overlay)
    d.rounded_rectangle([x0 - 6, y0 - 2, x0 + tw + 6, y0 + 14], radius=4, fill=(139, 69, 19, 220))
    base.alpha_composite(overlay)
    ImageDraw.Draw(base).text((x0, y0), label, font=fnt, fill=(255, 248, 230))


