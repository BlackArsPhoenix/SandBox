def render_page(n: int) -> Image.Image:
    src = Image.open(f"{PAGES_DIR}/page-{n:02d}.png").convert("RGBA")
    if src.size != (W, H):
        src = src.resize((W, H), Image.Resampling.LANCZOS)
    spec = SPECS.get(n, PageSpec(mode="keep"))

    if spec.mode == "banner":
        draw_banner(src, spec.banner or "")
    elif spec.mode == "panels":
        for panel in spec.panels:
            draw_panel(
                src,
                panel["box"],
                panel.get("title"),
                panel["body"],
                title_size=panel.get("title_size", 15),
                body_size=panel.get("body_size", 11),
                solid=True,
            )

    draw_footer_ru(src, n)
    return src.convert("RGB")


def main() -> None:
    os.makedirs(OUT_PAGES, exist_ok=True)
    paths = []
    for n in range(1, 33):
        print(f"render {n}/32")
        im = render_page(n)
        path = f"{OUT_PAGES}/ru-{n:02d}.png"
        im.save(path, "PNG", optimize=True)
        paths.append(path)

    # Build PDF; use JPEG intermediate for smaller size
    jpg_paths = []
    jpg_dir = f"{ROOT}/ru_jpg"
    os.makedirs(jpg_dir, exist_ok=True)
    for i, p in enumerate(paths, 1):
        jp = f"{jpg_dir}/ru-{i:02d}.jpg"
        Image.open(p).convert("RGB").save(jp, "JPEG", quality=85, optimize=True)
        jpg_paths.append(jp)

    with open(OUT_PDF, "wb") as f:
        f.write(img2pdf.convert(jpg_paths))
    size = os.path.getsize(OUT_PDF)
    print(f"Wrote {OUT_PDF} ({size/1024/1024:.1f} MB)")


if __name__ == "__main__":
    main()
